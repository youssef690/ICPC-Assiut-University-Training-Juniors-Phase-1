bool isprime(ll n){
    if(n==1) return false;
    for(int i=2 ; i*i<=n ; i++)
    {
        if(n%i==0) return false;
    }
    return true;
}

ll fastpower(ll x, ll k){
    ll ret = 1;
    while (k)
    {
        if (k & 1) ret = (ret*x) % mod;
        k >>= 1; x = (x*x) % mod;
    }
    return ret;
}

