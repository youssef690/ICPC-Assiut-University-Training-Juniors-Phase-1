bool open(char ch){
	return ch == '(' || ch == '{' || ch == '<' || ch == '[';
}
char rev(char ch){
	if (ch == ')')
		return '(';
	if (ch == '}')
		return '{';
	if (ch == ']')
		return '[';
	if (ch == '>')
		return '<';
}
