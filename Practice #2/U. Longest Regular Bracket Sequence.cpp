	int cnt = 1, mx = 0;
	for (int i = 0; i < sz(s); i++) {
		if (open(s[i]))
			st.push(i);
		else if (sz(st) && s[st.top()] == rev(s[i])) {
			tmp[i] = i - st.top() + 1;
			if (st.top())
				tmp[i] += tmp[st.top() - 1];
			if (tmp[i] > mx)
				mx = tmp[i], cnt = 1;
			else if (tmp[i] == mx)
				cnt++;
			st.pop();
		}
		else 
           st.push(i);
	}
